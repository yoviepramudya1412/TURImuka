                  children: [
                    CarouselSlider(
                      carouselController: controller.carouselController,
                      options: CarouselOptions(
                        height: MediaQuery.of(context).size.height * 0.8,
                        autoPlay: true,
                        enlargeCenterPage: true,
                        viewportFraction: 0.92,
